uint8_t state=0;
uint16_t cap_val[2]={0};
long high_time=0;
uint16_t count=0;
void Get_freq(void)
{
  if(state==0)
  {
    HAL_TIM_IC_Start_IT(&htim3,TIM_CHANNEL_1);
    __HAL_TIM_SET_CAPTUREPOLARITY(&htim3,TIM_CHANNEL_1,TIM_INPUTCHANNELPOLARITY_RISING);
    count=0;
  }
  else if(state==2)
  {
    high_time=10000*count+cap_val[1]-cap_val[0];
    UsbPrintf("High Time is : %.1f s\r\n",(float)high_time/10000);
    state=0;
  }
}

