float cap_uint_to_float(uint8_t cap1, uint8_t cap2)
{
	float convert = 0;
	uint16_t cap_config = 0;
	cap_config = ((cap2 & 0x07) << 5) | (cap1 >> 3);
	convert = 1.5 * ((cap2 >> 3) & 0x03) + cap_config * 0.051;
	return convert;
}

