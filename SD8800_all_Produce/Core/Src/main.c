void LedHeart(void)
{
    static uint32_t ledTick = 0;
    uint16_t Time = 1000;
    if (manager_get_mode() == FREE_MODE) {
        if (HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_7) == 0) {
          Time = 150;
        }
    }
    if (HAL_GetTick() > ledTick + Time) {
        ledTick = HAL_GetTick();
        HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_8);
    }
}
